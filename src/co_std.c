void CO_MemClear(void* ptr, size_t len)
{
    for (size_t index = 0; index < len; ++index)
    {
        ((uint8_t*)ptr)[index] = 0x00;
    }
}

void CO_MemCopy(void* to, const void* from, size_t len)
{
    for (size_t index = 0; index < len; ++index)
    {
        ((uint8_t*)to)[index] = ((uint8_t*)from)[index];
    }
}
